function toEs(en) {
  if (!en) return '';
  if (EN_ES[en]) return EN_ES[en];
  // Por defecto: dejar tal cual (la mayoría de capitales hispanas/africanas/oceánicas).
  return en;
}

(async () => {
  console.log('[capitals] fetching REST Countries…');
  const r = await fetch(REST_URL);
  if (!r.ok) throw new Error(`REST Countries HTTP ${r.status}`);
  const data = await r.json();
  const cap = {};
  for (const x of data) {
    if (x.cca2 && x.capital && x.capital[0]) cap[x.cca2] = x.capital[0];
  }

  const flags = JSON.parse(fs.readFileSync(FLAGS_PATH, 'utf8'));
  let updated = 0, missing = [];
  for (const f of flags) {
    const en = cap[f.iso];
    if (!en) { missing.push(f.iso); continue; }
    f.capital = toEs(en);
    updated++;
  }

  fs.writeFileSync(FLAGS_PATH, JSON.stringify(flags, null, 2) + '\n');
  console.log(`[capitals] OK · ${updated}/${flags.length} actualizados · sin capital: ${missing.length} ${missing.join(',')}`);
})();
